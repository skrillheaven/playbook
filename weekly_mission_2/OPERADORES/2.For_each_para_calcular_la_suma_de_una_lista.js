// Ejemplo 2: for Each para calcular la suma de todos los elementos de una list
let sum = 0
const numbers2 = [1, 2, 3, 4, 5]; //lista
numbers2.forEach(num => sum += num) //se lee la lista y se suman los datos en sum
console.log("Ejemplo2: Calculo de la suma de los numeros de una lista")
console.log(sum)